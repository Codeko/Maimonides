    public Tutor getTutor() {
        if (tutor != null) {
            tutor.setNombre(tfNombre.getText());
            tutor.setApellido1(tfApellido1.getText());
            tutor.setApellido2(tfApellido2.getText());
            tutor.setDni(tfDni.getText());
            tutor.setSexo(cbSexo.getSelectedIndex() == 0 ? "H" : "M");
        }
        return tutor;
    }

    public void setTutor(Tutor tutor) {
        this.tutor = tutor;
        if (tutor != null) {
            tfNombre.setText(tutor.getNombre());
            tfApellido1.setText(tutor.getApellido1());
            tfApellido2.setText(tutor.getApellido2());
            tfDni.setText(tutor.getDni());
            cbSexo.setSelectedIndex(tutor.getSexo().equals("M") ? 1 : 0);
        } else {
            tfNombre.setText("");
            tfApellido1.setText("");
            tfApellido2.setText("");
            tfDni.setText("");
            cbSexo.setSelectedIndex(0);
        }
    }

