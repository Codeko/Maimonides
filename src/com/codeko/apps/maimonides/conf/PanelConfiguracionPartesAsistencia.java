    @Action(block = Task.BlockingScope.ACTION)
    public Task guardarDatos() {
        return new GuardarDatosTask(org.jdesktop.application.Application.getInstance(com.codeko.apps.maimonides.MaimonidesApp.class));
    }

    private class GuardarDatosTask extends org.jdesktop.application.Task<Object, Void> {

        HashMap<String, String> datos = new HashMap<String, String>();
        ArrayList<Integer> tipos = new ArrayList<Integer>();

        GuardarDatosTask(org.jdesktop.application.Application app) {
            super(app);
            try {
                spDiasAviso.commitEdit();
                spCartasNumDias.commitEdit();
                spCartasDiasInicio.commitEdit();
            } catch (ParseException ex) {
                Logger.getLogger(PanelConfiguracionPartesAsistencia.class.getName()).log(Level.SEVERE, null, ex);
            }
            datos.put("partes_asistencia.comprimir", cbComprimirPartes.isSelected() ? "1" : "0");
            datos.put("faltas_aviso_justificacion", spDiasAviso.getValue().toString());
            datos.put("cartas_faltas_dia_semana", (cbCartasDiaSemana.getSelectedIndex() + 1) + "");
            datos.put("cartas_faltas_num_dias", spCartasNumDias.getValue().toString());
            datos.put("cartas_faltas_dias_inicio", spCartasDiasInicio.getValue().toString());
            datos.put(ConfiguracionAsistencia.AAE_PARAMETRO, cbAsistenciaExpulsados.getSelectedIndex() + "");

            if (cbI.isSelected()) {
                tipos.add(ParteFaltas.FALTA_INJUSTIFICADA);
            }
            if (cbJ.isSelected()) {
                tipos.add(ParteFaltas.FALTA_JUSTIFICADA);
            }
            if (cbE.isSelected()) {
                tipos.add(ParteFaltas.FALTA_EXPULSION);
            }
            if (cbR.isSelected()) {
                tipos.add(ParteFaltas.FALTA_RETRASO);
            }
            if (tipos.isEmpty()) {
                tipos.add(-1);
            }
            setMessage("Guardando datos...");
        }

        @Override
        protected Object doInBackground() {
            Iterator it = datos.keySet().iterator();
            while (it.hasNext()) {
                String nombre = it.next().toString();
                String valor = datos.get(nombre);
                MaimonidesApp.getApplication().getConfiguracion().set(nombre, valor);
            }
            //Para los tipos de faltas usamos directamente la clase
            ConfiguracionPerdidaEscolaridad.setTipoFaltasContabilizables(tipos);
            return null;
        }

        @Override
        protected void succeeded(Object result) {
            setMessage("Datos guardados correctamente.");
        }
    }

    @Action(block = Task.BlockingScope.APPLICATION)
    public final Task cargarDatos() {
        return new CargarDatosTask(org.jdesktop.application.Application.getInstance(com.codeko.apps.maimonides.MaimonidesApp.class));
    }

    private class CargarDatosTask extends org.jdesktop.application.Task<Object, Void> {

        CargarDatosTask(org.jdesktop.application.Application app) {
            super(app);
        }

        @Override
        protected Object doInBackground() {
            Configuracion cfg = MaimonidesApp.getApplication().getConfiguracion();
            cbComprimirPartes.setSelected(cfg.get("partes_asistencia.comprimir", "0").equals("1"));
            spDiasAviso.setValue(Num.getInt(cfg.get("faltas_aviso_justificacion", "100")));
            ArrayList<Integer> tipos = ConfiguracionPerdidaEscolaridad.getTiposFaltasContabilizables();
            cbI.setSelected(tipos.contains(ParteFaltas.FALTA_INJUSTIFICADA));
            cbE.setSelected(tipos.contains(ParteFaltas.FALTA_EXPULSION));
            cbR.setSelected(tipos.contains(ParteFaltas.FALTA_RETRASO));
            cbJ.setSelected(tipos.contains(ParteFaltas.FALTA_JUSTIFICADA));
            cbCartasDiaSemana.setSelectedIndex(Num.getInt(cfg.get("cartas_faltas_dia_semana", "5")) - 1);
            spCartasNumDias.setValue(Num.getInt(cfg.get("cartas_faltas_num_dias", "7")));
            spCartasDiasInicio.setValue(Num.getInt(cfg.get("cartas_faltas_dias_inicio", "11")));
            cbAsistenciaExpulsados.setSelectedIndex(Num.getInt(cfg.get(ConfiguracionAsistencia.AAE_PARAMETRO, ConfiguracionAsistencia.AAE_ASIGNAR_EXPULSION + "")));

            return null;
        }

        @Override
        protected void succeeded(Object datos) {
            setMessage("Datos cargados correctamente.");
        }
    }
