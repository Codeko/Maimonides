    public static int getTotalPartes(int idProfesor) {
        int total = 0;
        PreparedStatement st = null;
        ResultSet res = null;
        try {
            String sql = "select count(*) AS total FROM partes AS p "
                    + " JOIN partes_horarios AS pa ON pa.parte_id=p.id JOIN horarios AS h ON h.id=pa.horario_id JOIN profesores AS pf ON pf.id=h.profesor_id "
                    + " JOIN partes_imagenes AS pi ON pi.parte_id=p.id "
                    + " WHERE p.ano=? AND h.ano=? AND pf.ano=? AND pa.firmado=0 AND pa.dividido=0 AND p.digitalizado=1 AND pf.id=? "
                    + " ORDER BY p.fecha,p.id";
            st = (PreparedStatement) MaimonidesApp.getApplication().getConector().getConexion().prepareStatement(sql);
            st.setInt(1, MaimonidesApp.getApplication().getAnoEscolar().getId());
            st.setInt(2, MaimonidesApp.getApplication().getAnoEscolar().getId());
            st.setInt(3, MaimonidesApp.getApplication().getAnoEscolar().getId());
            st.setInt(4, idProfesor);
            res = st.executeQuery();
            if (res.next()) {
                total = res.getInt("total");
            }
        } catch (SQLException ex) {
            Logger.getLogger(PanelPartesNoFirmados.class.getName()).log(Level.SEVERE, null, ex);
        }

        Obj.cerrar(res, st);
        return total;
    }

    private class ActualizarPartesTask extends org.jdesktop.application.Task<Object, Void> {

        Profesor p = null;

        ActualizarPartesTask(org.jdesktop.application.Application app) {
            super(app);
            modelo.vaciar();
            setProgress(1);
            int row = tablaProfesores.getSelectedRow();
            if (row > -1) {
                ProfesorPartesNoFirmado pf = modeloProfesores.getElemento(tablaProfesores.convertRowIndexToModel(row));
                p = pf.getProfesor();
            }
        }

        @Override
        protected Object doInBackground() {
            if (p != null) {
                PreparedStatement st = null;
                ResultSet res = null;
                try {
                    int total = getTotalPartes(p.getId());
                    String sql = "select distinct p.* FROM partes_horarios AS pa "
                            + " JOIN partes AS p ON pa.parte_id=p.id JOIN horarios AS h ON h.id=pa.horario_id JOIN profesores AS pf ON pf.id=h.profesor_id "
                            + " JOIN partes_imagenes AS pi ON pi.parte_id=p.id "
                            + " WHERE p.ano=? AND h.ano=? AND pf.ano=? AND pa.firmado=0 AND pa.dividido=0  AND p.digitalizado=1 AND pf.id=? GROUP BY p.id "
                            + " ORDER BY p.fecha,p.id";
                    st = (PreparedStatement) MaimonidesApp.getApplication().getConector().getConexion().prepareStatement(sql);
                    st.setInt(1, MaimonidesApp.getApplication().getAnoEscolar().getId());
                    st.setInt(2, MaimonidesApp.getApplication().getAnoEscolar().getId());
                    st.setInt(3, MaimonidesApp.getApplication().getAnoEscolar().getId());
                    st.setInt(4, p.getId());
                    res = st.executeQuery();
                    int pos = 0;
                    while (res.next() && !isCancelled()) {
                        pos++;
                        setMessage(String.format("Cargando partes de asistencias %d de %d...", pos, total));
                        setProgress(pos, 0, total);
                        ParteFaltas pFaltas = new ParteFaltas();
                        pFaltas.cargarDesdeResultSet(res);
                        modelo.addDato(pFaltas);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(PanelPartesNoFirmados.class.getName()).log(Level.SEVERE, null, ex);
                } finally {
                    Obj.cerrar(st, res);
                }
            }
            return null;
        }

        @Override
        protected void succeeded(Object result) {
            tabla.packAll();
        }
    }

    private class ActualizarProfesoresTask extends org.jdesktop.application.Task<Object, Void> {

        ActualizarProfesoresTask(org.jdesktop.application.Application app) {
            super(app);
            modeloProfesores.vaciar();
            setProgress(1);
        }

        @Override
        protected Object doInBackground() {
            PreparedStatement st = null;
            ResultSet res = null;
            try {
                int total = MaimonidesUtil.getCount("profesores");
                String extra = "";
                Profesor fp = null;
                if (Permisos.isUsuarioSoloProfesor()) {
                    fp = Permisos.getFiltroProfesor();
                    if (fp != null) {
                        extra = " AND pf.id=? ";
                    }
                }
                String sql = "select pf.*,count(*) AS noFirmados FROM partes_horarios AS pa "
                        + " JOIN partes AS p ON pa.parte_id=p.id JOIN horarios AS h ON h.id=pa.horario_id JOIN profesores AS pf ON pf.id=h.profesor_id "
                        + " JOIN partes_imagenes AS pi ON pi.parte_id=p.id "
                        + " WHERE p.ano=? AND h.ano=? AND pf.ano=? AND pa.firmado=0 AND pa.dividido=0 AND p.digitalizado=1 " + extra
                        + " GROUP BY h.profesor_id ORDER BY count(*) DESC";
                st = (PreparedStatement) MaimonidesApp.getApplication().getConector().getConexion().prepareStatement(sql);
                st.setInt(1, MaimonidesApp.getApplication().getAnoEscolar().getId());
                st.setInt(2, MaimonidesApp.getApplication().getAnoEscolar().getId());
                st.setInt(3, MaimonidesApp.getApplication().getAnoEscolar().getId());
                if (fp != null) {
                    st.setInt(4, fp.getId());
                }
                res = st.executeQuery();
                int pos = 0;
                while (res.next() && !isCancelled()) {
                    pos++;
                    setMessage(String.format("Cargando profesor %d de %d...", pos, total));
                    setProgress(pos, 0, total);
                    Profesor p = new Profesor();
                    p.cargarDesdeResultSet(res);
                    ProfesorPartesNoFirmado pn = new ProfesorPartesNoFirmado(p, res.getInt("noFirmados"));
                    modeloProfesores.addDato(pn);
                }
            } catch (Exception ex) {
                Logger.getLogger(PanelPartesNoFirmados.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                Obj.cerrar(st, res);
            }
            return null;
        }

        @Override
        protected void succeeded(Object result) {
            tablaProfesores.packAll();
            setCargado(true);
        }
    }
