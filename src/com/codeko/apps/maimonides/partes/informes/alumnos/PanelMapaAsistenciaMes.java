    @Override
    public void cargar() {
        for (PanelMapaAsistenciaBloque pb : bloques) {
            pb.setAlumno(getAlumno());
            pb.cargar();
        }
        setCargado(true);
    }

    @Override
    public void vaciar() {
        setAlumno(null);
        for (PanelMapaAsistenciaBloque pb : bloques) {
            pb.vaciar();
        }
        setCargado(false);
    }

    @Override
    public boolean isCargado() {
        return cargado;
    }

