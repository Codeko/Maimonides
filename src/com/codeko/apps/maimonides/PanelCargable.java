    private void operarComponentes(JComponent comp, boolean cargar) {
        for (Component c : comp.getComponents()) {
            if (c instanceof ICargable) {
                if (cargar) {
                    ((ICargable) c).cargar();
                } else {
                    ((ICargable) c).vaciar();
                }
            }
            if (c instanceof JComponent) {
                operarComponentes((JComponent) c, cargar);
            }
        }
    }

