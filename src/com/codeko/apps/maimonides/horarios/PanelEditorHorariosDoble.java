    @Action
    public void dividir() {
        if (dividido) {
            remove(split);
            add(panelEditorHorarios1, BorderLayout.CENTER);
            bDividir.setIcon(resourceMap.getIcon("dividir.Action.icon"));
            bDividir.setText(resourceMap.getString("dividir.Action.text"));
        } else {
            remove(panelEditorHorarios1);
            split.setBottomComponent(panelEditorHorarios2);
            split.setTopComponent(panelEditorHorarios1);
            add(split, BorderLayout.CENTER);
            bDividir.setIcon(resourceMap.getIcon("dividir.Action.icon2"));
            bDividir.setText(resourceMap.getString("dividir.Action.text2"));
        }
        dividido = !dividido;
        updateUI();
    }
